var isValidSudoku = function(board) {
	let row = [];
	let col = [];
	let block = [];
	for (let i = 0; i < board.length; i++) {
		row.push({});
		col.push({});
		block.push({});
	}
	for (let i = 0; i < board.length; i++) {
		for (let j = 0; j < board.length; j++) {
			if (board[i][j] !== ".") {
				const key = board[i][j];
				const index = Math.floor(i / 3) * 3 + Math.floor(j / 3);
				if (!!row[i][key] || !!col[j][key] || !!block[index][key]) {
					return false;
				}
				row[i][key] = true;
				col[j][key] = true;
				block[index][key] = true;
			}
		}
	}
	return true;
};
